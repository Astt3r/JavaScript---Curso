//operadores aritmeticos

let a = 5;
let b = 7;

console.log(a + b, 'adicion');//suma
console.log(a - b, 'sustraccion');//resta
console.log(a * b, 'multiplicacion');//multiplicacion
console.log(a / b, 'division');//division
console.log(a % b, 'modulo o resto');//modulo o resto
console.log(a ** b, 'exponente');//exponente

//incrementar
console.log(++a);//incremento pre
console.log(a++);//incremento post
console.log(a);//valor de a despues del incremento post

//decrementar

console.log(--b);//decremento pre
console.log(b--);   //decremento post
console.log(b); //valor de b despues del decremento post